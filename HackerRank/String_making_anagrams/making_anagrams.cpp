using namespace std;

int number_needed(string a, string b) {
    std::vector<int> v(26,0);
    int ans = 0;
    for (auto i : a)    v[i - 'a'] += 1;
    for (auto j : b)    v[j - 'a'] -= 1;
    for (auto k : v)    ans += abs(k);
    return ans;
}

int main(){
    string a;
    cin >> a;
    string b;
    cin >> b;
    cout << number_needed(a, b) << endl;
    return 0;
}

